/*
* bti_write_int
* Writes a 4 byte integer to the ofstream - little endian.
*/
void bti_write_int(unsigned int aiInt, std::ofstream& out)
{
#ifdef _DEBUG
   std::cout << "Writing Int: " << aiInt << std::endl;
#endif // DEBUG

   // Writes little endian.
   for( int i = 0; i < 4; i++ )
   {
      char c = (aiInt >> i*8) & 0xFF;
      out.write(&c, 1);
   }
}

int main(int argc, char* argv[])
{
   /*
   * Expects 2 arguments.
   * <input file name> <output file name>
   */
   char* szFileName;
   char* szOutputFileName;

   if( argc < 3 )
   {
      std::cout << "Not enough arguments." << std::endl;
      std::cout << "Syntax: <Input File Name> <Output File Name>" << std::endl;
      std::cin.get();
      return -1;
   }

   // Open the input binary
   szFileName = argv[1];
   std::ifstream input_file(szFileName, std::ios::ate | std::ios::binary);
   if( !input_file.good() )
   {
      std::cout << "Could not open input file." << std::endl;
      std::cin.get();
      return -2;
   }

   // Compute the GP Header
   TI_GP_Header GP_Header;
   GP_Header.image_size = input_file.tellg();
   GP_Header.load_addr = 0x402F0400;

   // Write to the output file in the following order
   // 1. TOC
   // 2. GP Header
   // 3. Image input
   szOutputFileName = argv[2];

   // Need to open with ios::binary or '0D' aka '\r's will magically be added.
   std::ofstream output_file(szOutputFileName, std::ios::binary);
   
   // Insert the TOC.
   output_file.write((char *)TOC_Data, 512);

   // Insert the GP Header
   bti_write_int(GP_Header.image_size, output_file);
   bti_write_int(GP_Header.load_addr, output_file);

   // Write the image
   char ch;

   // Go back to the beginning of the input file.
   input_file.clear();
   input_file.seekg(0, std::ios::beg);
   while( input_file.read(&ch, 1) )
   {
      output_file.write(&ch, 1);
   }

   // Close the file.
   output_file.close();

#ifdef _DEBUG
   std::cout << "Input File Size: " << GP_Header.image_size  << std::endl;
   std::cin.get();
#endif // DEBUG

   return 0;
}


